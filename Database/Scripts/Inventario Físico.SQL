CREATE TABLE dbo.INVENTORY_COUNT
(
    INVENTORY_COUNT_ID BIGINT IDENTITY(1,1) NOT NULL,
    BUSINESS_ID BIGINT NOT NULL,
    WAREHOUSE_ID BIGINT NOT NULL,

    COUNT_NUMBER VARCHAR(30) NOT NULL,
    COUNT_DATE DATE NOT NULL,

    INVENTORY_COUNT_STATUS_ID BIGINT NOT NULL,

    OBSERVATION NVARCHAR(500) NULL,

    CREATE_USER BIGINT NOT NULL,
    CREATE_DATE DATETIME NOT NULL CONSTRAINT DF_INVENTORY_COUNT_CREATE_DATE DEFAULT(GETDATE()),
    UPDATE_USER BIGINT NULL,
    UPDATE_DATE DATETIME NULL,
    STATUS BIT NOT NULL CONSTRAINT DF_INVENTORY_COUNT_STATUS DEFAULT(1),

    CONSTRAINT PK_INVENTORY_COUNT
        PRIMARY KEY (INVENTORY_COUNT_ID),

    CONSTRAINT FK_INVENTORY_COUNT_BUSINESS
        FOREIGN KEY (BUSINESS_ID) REFERENCES dbo.BUSINESS(BUSINESS_ID),

    CONSTRAINT FK_INVENTORY_COUNT_WAREHOUSE
        FOREIGN KEY (WAREHOUSE_ID) REFERENCES dbo.WAREHOUSES(WAREHOUSE_ID),

    CONSTRAINT FK_INVENTORY_COUNT_STATUS
        FOREIGN KEY (INVENTORY_COUNT_STATUS_ID)
        REFERENCES dbo.INVENTORY_COUNT_STATUS(INVENTORY_COUNT_STATUS_ID)
);

CREATE TABLE dbo.INVENTORY_COUNT_DETAIL
(
    INVENTORY_COUNT_DETAIL_ID BIGINT IDENTITY(1,1) NOT NULL,
    BUSINESS_ID BIGINT NOT NULL,
    INVENTORY_COUNT_ID BIGINT NOT NULL,
    PRODUCTS_ID BIGINT NOT NULL,

    SYSTEM_QUANTITY DECIMAL(18,4) NOT NULL CONSTRAINT DF_INVENTORY_COUNT_DETAIL_SYSTEM_QTY DEFAULT(0),
    COUNTED_QUANTITY DECIMAL(18,4) NOT NULL CONSTRAINT DF_INVENTORY_COUNT_DETAIL_COUNTED_QTY DEFAULT(0),
    DIFFERENCE_QUANTITY AS (COUNTED_QUANTITY - SYSTEM_QUANTITY) PERSISTED,

    UNIT_COST DECIMAL(18,4) NOT NULL CONSTRAINT DF_INVENTORY_COUNT_DETAIL_UNIT_COST DEFAULT(0),
    TOTAL_DIFFERENCE_COST AS ((COUNTED_QUANTITY - SYSTEM_QUANTITY) * UNIT_COST) PERSISTED,

    LOT_NUMBER NVARCHAR(100) NULL,
    SERIAL_NUMBER NVARCHAR(100) NULL,
    EXPIRATION_DATE DATE NULL,

    ADJUSTMENT_MOVEMENT_ID BIGINT NULL,

    OBSERVATION NVARCHAR(500) NULL,

    CREATE_USER BIGINT NOT NULL,
    CREATE_DATE DATETIME NOT NULL CONSTRAINT DF_INVENTORY_COUNT_DETAIL_CREATE_DATE DEFAULT(GETDATE()),
    UPDATE_USER BIGINT NULL,
    UPDATE_DATE DATETIME NULL,
    STATUS BIT NOT NULL CONSTRAINT DF_INVENTORY_COUNT_DETAIL_STATUS DEFAULT(1),

    CONSTRAINT PK_INVENTORY_COUNT_DETAIL
        PRIMARY KEY (INVENTORY_COUNT_DETAIL_ID),

    CONSTRAINT FK_INVENTORY_COUNT_DETAIL_BUSINESS
        FOREIGN KEY (BUSINESS_ID) REFERENCES dbo.BUSINESS(BUSINESS_ID),

    CONSTRAINT FK_INVENTORY_COUNT_DETAIL_COUNT
        FOREIGN KEY (INVENTORY_COUNT_ID) REFERENCES dbo.INVENTORY_COUNT(INVENTORY_COUNT_ID),

    CONSTRAINT FK_INVENTORY_COUNT_DETAIL_PRODUCT
        FOREIGN KEY (PRODUCTS_ID) REFERENCES dbo.PRODUCTS(PRODUCTS_ID),

    CONSTRAINT FK_INVENTORY_COUNT_DETAIL_ADJUSTMENT_MOVEMENT
        FOREIGN KEY (ADJUSTMENT_MOVEMENT_ID)
        REFERENCES dbo.WAREHOUSE_MOVEMENT(WAREHOUSE_MOVEMENT_ID)
);

CREATE TABLE dbo.INVENTORY_COUNT_STATUS
(
    INVENTORY_COUNT_STATUS_ID BIGINT IDENTITY(1,1) NOT NULL,
    CODE VARCHAR(30) NOT NULL,
    DESCRIPTION NVARCHAR(100) NOT NULL,

    CREATE_USER BIGINT NOT NULL,
    CREATE_DATE DATETIME NOT NULL CONSTRAINT DF_INVENTORY_COUNT_STATUS_CREATE_DATE DEFAULT(GETDATE()),
    UPDATE_USER BIGINT NULL,
    UPDATE_DATE DATETIME NULL,
    STATUS BIT NOT NULL CONSTRAINT DF_INVENTORY_COUNT_STATUS_STATUS DEFAULT(1),

    CONSTRAINT PK_INVENTORY_COUNT_STATUS
        PRIMARY KEY (INVENTORY_COUNT_STATUS_ID),

    CONSTRAINT UQ_INVENTORY_COUNT_STATUS_CODE
        UNIQUE (CODE)
);

